<template>
  <div>
    <v-data-table
      :headers="headers"
      :items="categories"
      :search="search"
      :items-per-page="15"
      sort-by="id"
      class="elevation-0 "
    >
      <template v-slot:top>
        <v-toolbar flat color="gray">
          <v-toolbar-title
            ><h3>
              Medicine categories
            </h3></v-toolbar-title
          >
          <v-spacer></v-spacer>

          <v-text-field
            v-model="search"
            label="Search by name/company/composition"
            single-line
            hide-details
            background-color="#EFEFEF"
            rounded
            height="40"
            class="shrink"
          ></v-text-field
          >&nbsp;&nbsp;
          <v-dialog v-model="dialog" max-width="900px">
            <template v-slot:activator="{ on, attrs }">
              <v-btn
                color="teal"
                elevation="1"
                fab
                small
                class="mb-2"
                v-bind="attrs"
                v-on="on"
                dark
                ><v-icon>mdi-plus</v-icon></v-btn
              >
            </template>
            <v-card>
              <v-card-title>
                <span class="headline">{{ formTitle }}</span>
              </v-card-title>

              <v-card-text>
                <v-container>
                  <v-row>
                    <v-col cols="12" sm="6" md="4">
                      <v-text-field
                        v-model="editedItem.name"
                        label="Name"
                      ></v-text-field>
                    </v-col>
                    <v-col cols="12" sm="6" md="8">
                      <v-text-field
                        v-model="editedItem.descriptions"
                        label="Descriptions"
                      ></v-text-field>
                    </v-col>
                  </v-row>
                </v-container>
              </v-card-text>

              <v-card-actions>
                <v-spacer></v-spacer>
                <v-btn color="blue darken-1" text @click="close">Cancel</v-btn>
                <v-btn color="blue darken-1" text @click="save">Save</v-btn>
              </v-card-actions>
            </v-card>
          </v-dialog>
        </v-toolbar>
      </template>
      <template v-slot:[`item.actions`]="{ item }">
        <v-icon small class="mr-2" @click="editItem(item)"> mdi-pencil </v-icon>
        <v-icon small color="indigo darken-4" @click="deleteItem(item)">
          mdi-delete
        </v-icon>
      </template>
      <template v-slot:no-data>
        <v-progress-linear indeterminate color="cyan"></v-progress-linear>
      </template>
    </v-data-table>
  </div>
</template>
<script>
export default {
  props: {
    categories: {
      type: Array,
      default: null
    }
  },
  data: () => {
    return {
      editedIndex: -1,
      editedItemId: "",
      dialog: false,
      search: "",
      headers: [
        { text: "ID", value: "id" },
        { text: "Name", value: "name" },
        { text: "Descriptions", value: "descriptions", sortable: false },
        { text: "Actions", value: "actions", sortable: false }
      ],
      editedItem: {
        id: 0,
        name: "",
        descriptions: ""
      },
      defaultItem: {
        id: 0,
        name: "",
        descriptions: ""
      }
    };
  },
  methods: {
    save() {
      if (this.editedIndex > -1) {
        Object.assign(this.categories[this.editedIndex], this.editedItem);
        console.log(this.editedItem);
        //this.$store.dispatch("update_medicine_category", this.editedItem);
      } else {
        this.categories.push(this.editedItem);
        this.$store.dispatch("add_new_medicine_category", this.editedItem);
        this.$emit("update");
      }
      this.close();
    },
    close: function() {
      this.dialog = false;
    },
    editItem: function(item) {
      this.editedIndex = this.categories.indexOf(item);
      this.editedItem = Object.assign({}, item);
      this.dialog = true;
      this.editedItemId = item.id;
    },
    deleteItem: function() {}
  },
  computed: {
    formTitle() {
      return this.editedIndex === -1
        ? "New medicine  category"
        : "Edit medicine category";
    }
  }
};
</script>
